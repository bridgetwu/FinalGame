package com.game.models;

import java.awt.*;

public class Dimensions {
    int x;
    int y;
    int imgWidth;
    int imgHeight;
    Image image;

    protected void setX() {
        this.x = x;
    }

    protected int getX() {
        return x;
    }

    protected void setY() {
        this.y = y;
    }

    protected int getY() {
        return y;
    }

    protected int getImgWidth() {
        return imgWidth;
    }

    protected int getImgHeight() {
        return imgHeight;
    }

    protected Image getImage() {
        return image;
    }
}
